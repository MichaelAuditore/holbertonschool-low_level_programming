#include "3-calc.h"
/**
 * op_mul - do a multiply
 * @a: First Integer
 * @b: Second Integer
 * Return: Result of multiply
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - do a division
 * @a: First Integer
 * @b: Second Integer
 * Return: Result of division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - do a module
 * @a: First Integer
 * @b: Second Integer
 * Return: Result of module
 */
int op_mod(int a, int b)
{
	return (a % b);
}
/**
 * op_sub - do a rest
 * @a: First Integer
 * @b: Second Integer
 * Return: Result of rest
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_add - do a sum
 * @a: First integer
 * @b: Second integer
 * Return: Result of sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
